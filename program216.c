#include<stdio.h>
#include<string.h>

void  Display(char str[])
{
   while(*str !='\0')
   {
   printf("%c\n",*str);
   str++;
   }

}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50]={'\0'};
    printf("Enter String :\n");
    scanf("%[^'\n']s",Arr);

    Display(Arr);

    return 0;
}  

